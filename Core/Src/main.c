  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)evspin.adc.buffers.ADC1_reg_raw, ADC1_REG_CHANNELS);
  HAL_ADC_Start_DMA(&hadc2, (uint32_t*)evspin.adc.buffers.ADC2_reg_raw, ADC2_REG_CHANNELS);
